//Функция ввода чисел с проверкой, что они от min до max
int console_read_int(string text, int min, int max)
{
    Console.Write($"{text}: ");
    
    int read_int;

    while(true)
    {
        
        if( int.TryParse(Console.ReadLine(), out read_int ) )
        {
            if(read_int >= min && read_int <= max)
            {
                return read_int;
            }
            else
            {
                Console.Write($"Число должно находиться в пределах от {min} до {max}, повторите попытку ввода:");
            }
        }
        else
        {
            Console.Write("Введено некорректное число, повторите попытку ввода:");
        }
        
    }
}
//Функция запрашивает ввести элементы для заполнения массива array
void console_read_array_elements(int[] array)
{
    for(int i=0; i < array.Length; i++)
    {
        array[i] = console_read_int($"Введите элемент массива №{i+1} ", 1, 31);
    }
}
//Возвращает строку YES если четных не меньше чем нечетных, и NO если наоборот
string yes_or_no(int count_even, int count_no_even)
{
    if(count_even >= count_no_even) return "YES";
    else return "NO";
}

//Выдает строкус результатом
string get_result(int[] array)
{
    string not_even = string.Empty;
    string even = string.Empty;
    int count_not_even = 0;
    int count_even = 0;

    foreach(int a in array)
    {
        if(a % 2 == 0) 
        {
            even += $"{a} ";
            count_even++;
        }
        else
        {
            not_even += $"{a} ";
            count_not_even++;
        }
    }

    return $"{not_even}\n{even}\n{yes_or_no(count_even, count_not_even)} ";
}

int N = console_read_int("Введите кол-во элементов в массиве ", 1, 1000);

//Создаем массив для хранения чисел
int[] array = new int[N];

//Заполняем его вводом из консоли.
console_read_array_elements(array);

//Выводим результат
Console.WriteLine( get_result(array) );


